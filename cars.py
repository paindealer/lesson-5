class car:
    def __init__(self, color, age):
        self.color = color
        self.age = age

class diesel_car:
    def __init__(self, fuel, color, age):
        self.diesel = fuel
        self.color = color
        self.age = age

class electric_car:
    def __init__(self, fuel, color, age ):
        self.electric = fuel
        self.color = color
        self.age = age